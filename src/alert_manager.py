"""
alert_manager.py -- Centralised Alert Storage and Export
=========================================================
This module is the "single source of truth" for all security alerts
generated by the AI Security Log Analyzer.  It collects alerts from
two independent detection systems:

    1. **ML model** (Isolation Forest) -- statistical anomaly detection
    2. **Rule engine** -- deterministic, threshold-based rules

Both types of alert are written into a single SQLite database so that
the dashboard (Step 5) and any future tooling can query them with
standard SQL.  An additional JSON export is produced for easy sharing
or integration with external systems (e.g. a SIEM or ticketing tool).

Output files:
    data/alerts.db    -- SQLite database with an "alerts" table
    data/alerts.json  -- JSON array of all alerts (human-readable)

Database schema (alerts table):
    id             INTEGER PRIMARY KEY   -- auto-incrementing row ID
    timestamp      TEXT                  -- when the suspicious event occurred
    alert_type     TEXT                  -- "ml" or "rule"
    severity       TEXT                  -- "low" / "medium" / "high" / "critical"
    affected_user  TEXT                  -- which user account was involved
    source_ip      TEXT                  -- the IP address under suspicion
    anomaly_score  REAL                  -- ML model's decision-function score
                                            (only meaningful for ML alerts;
                                             NULL for rule-based alerts)
    rule_name      TEXT                  -- which rule fired
                                            (NULL for ML alerts)
    description    TEXT                  -- human-readable explanation

Usage:
    python src/alert_manager.py          # standalone: runs detect + rules + store
    from src.alert_manager import ...    # import individual functions
"""

